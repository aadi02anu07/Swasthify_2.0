// Report 1 — Patient Summary
// Demographics + latest vitals + vitals averages + medical history + appointments
// ═════════════════════════════════════════════════════════════════════════════

const buildPatientSummary = (doc, {
  patient, demographics, latestVitals,
  vitalsSummary, medicalHistory, appointments,
  generatedBy, hospitalName,
}) => {
  let y      = pageHeader(doc, hospitalName);
  let pageNum = 1;

  // Report title
  y += 14;
  doc.font(FONT.bold).fontSize(14).fillColor(C.navy)
     .text("Patient Medical Summary Report", MARGIN, y);
  doc.font(FONT.regular).fontSize(8).fillColor(C.muted)
     .text(
       `Generated: ${fDateTime(new Date())}   ·   Prepared by: ${generatedBy}`,
       MARGIN, y + 18
     );
  y += 38;
  hRule(doc, y);
  y += 14;

  // ── 1. Demographics ──────────────────────────────────────────────────────
  y = sectionTitle(doc, y, "Patient Information");
  y += 10;

  const col1 = MARGIN;
  const col2 = MARGIN + PW / 2;

  labelValue(doc, col1, y,       "Patient ID:",          patient.patientID);
  labelValue(doc, col2, y,       "Blood Group:",         patient.bloodGroup ?? "Not recorded");
  y += 16;
  labelValue(doc, col1, y,       "Full Name:",           patient.name);
  labelValue(doc, col2, y,       "Gender:",              patient.gender ?? "Not recorded");
  y += 16;
  labelValue(doc, col1, y,       "Date of Birth:",       fDate(patient.dob));
  labelValue(doc, col2, y,       "Age:",                 `${demographics.age} years`);
  y += 16;
  labelValue(doc, col1, y,       "Phone:",               patient.phone ?? "Not recorded");
  labelValue(doc, col2, y,       "Emergency Contact:",   patient.emergencyContact ?? "Not recorded");
  y += 20;
  hRule(doc, y);
  y += 14;

  // ── 2. Latest Vitals ─────────────────────────────────────────────────────
  y = sectionTitle(doc, y, "Latest Vitals Reading");
  y += 10;

  if (latestVitals) {
    doc.font(FONT.regular).fontSize(8).fillColor(C.muted)
       .text(
         `Recorded: ${fDateTime(latestVitals.recordedAt)}   ·   By: ${latestVitals.recordedBy?.name ?? "—"}   ·   ${latestVitals.hospital?.name ?? ""}`,
         MARGIN, y
       );
    y += 14;

    const half = 3;
    const vCols = [
      ["Blood Pressure",   `${latestVitals.bpSystolic}/${latestVitals.bpDiastolic} mmHg`],
      ["Heart Rate",       `${latestVitals.heartRate} bpm`],
      ["Blood Sugar",      `${latestVitals.sugar} mg/dL`],
      ["SpO₂",            latestVitals.spo2        != null ? `${latestVitals.spo2}%`    : "Not recorded"],
      ["Temperature",      latestVitals.temperature != null ? `${latestVitals.temperature}°C` : "Not recorded"],
      ["Weight",           latestVitals.weight      != null ? `${latestVitals.weight} kg` : "Not recorded"],
    ];
    vCols.slice(0, half).forEach(([l, v], i) => labelValue(doc, col1, y + i * 16, l + ":", v));
    vCols.slice(half).forEach(([l, v], i)    => labelValue(doc, col2, y + i * 16, l + ":", v));
    y += half * 16 + 10;

    // Averages row
    if (vitalsSummary?.totalReadings > 1) {
      const avg = vitalsSummary.averages;
      y += 6;
      doc.font(FONT.bold).fontSize(8).fillColor(C.text)
         .text(`Averages across ${vitalsSummary.totalReadings} readings:`, MARGIN, y);
      y += 12;
      y = drawTable(doc, y, {
        headers:   ["Avg BP (mmHg)", "Avg HR (bpm)", "Avg Sugar (mg/dL)", "Avg SpO₂ (%)", "Avg Temp (°C)"],
        rows: [[
          `${avg.bpSystolic ?? "—"}/${avg.bpDiastolic ?? "—"}`,
          avg.heartRate    ?? "—",
          avg.sugar        ?? "—",
          avg.spo2         ?? "—",
          avg.temperature  ?? "—",
        ]],
        colWidths: Array(5).fill(PW / 5),
        rowHeight: 22,
      });
      y += 4;
    }
  } else {
    doc.font(FONT.regular).fontSize(8).fillColor(C.muted)
       .text("No vitals recorded yet.", MARGIN, y);
    y += 18;
  }

  y += 10;
  hRule(doc, y);
  y += 14;

  // ── 3. Medical History ────────────────────────────────────────────────────
  if (y + 26 + Math.min(medicalHistory.length, 5) * 22 > 760) {
    pageFooter(doc, pageNum++);
    doc.addPage();
    y = pageHeader(doc, hospitalName) + 14;
  }

  y = sectionTitle(doc, y, `Medical History   (${medicalHistory.length} records)`);
  y += 10;

  if (medicalHistory.length === 0) {
    doc.font(FONT.regular).fontSize(8).fillColor(C.muted)
       .text("No medical history recorded.", MARGIN, y);
    y += 18;
  } else {
    const histRows = medicalHistory.map((h) => [
      fDate(h.occurredAt),
      h.type.replace("_", " ").toUpperCase(),
      h.title,
      h.description.length > 55 ? h.description.slice(0, 52) + "..." : h.description,
      { text: h.severity ?? "—", color: severityColor(h.severity) },
      h.hospital?.name ?? "—",
    ]);

    // Paginate if needed
    const ROWS_PER_PAGE = 24;
    for (let ci = 0; ci < histRows.length; ci += ROWS_PER_PAGE) {
      if (ci > 0) {
        pageFooter(doc, pageNum++);
        doc.addPage();
        y = pageHeader(doc, hospitalName) + 14;
      }
      y = drawTable(doc, y, {
        headers:   ["Date", "Type", "Title", "Description", "Severity", "Hospital"],
        rows:      histRows.slice(ci, ci + ROWS_PER_PAGE),
        colWidths: [65, 75, 100, 145, 55, 55],
        rowHeight: 22,
      });
      y += 4;
    }
  }

  y += 10;
  hRule(doc, y);
  y += 14;

  // ── 4. Appointments ────────────────────────────────────────────────────────
  if (y + 26 + Math.min(appointments.length, 3) * 22 > 760) {
    pageFooter(doc, pageNum++);
    doc.addPage();
    y = pageHeader(doc, hospitalName) + 14;
  }

  y = sectionTitle(doc, y, `Upcoming Appointments   (${appointments.length})`);
  y += 10;

  if (appointments.length === 0) {
    doc.font(FONT.regular).fontSize(8).fillColor(C.muted)
       .text("No upcoming appointments.", MARGIN, y);
    y += 18;
  } else {
    y = drawTable(doc, y, {
      headers:   ["Scheduled At", "Doctor", "Status", "Reason", "Hospital"],
      rows:      appointments.map((a) => [
        fDateTime(a.scheduledAt),
        a.staff?.name ?? "—",
        a.status.toUpperCase(),
        a.reason ?? "—",
        a.hospital?.name ?? "—",
      ]),
      colWidths: [130, 100, 70, 130, 65],
      rowHeight: 22,
    });
  }

  pageFooter(doc, pageNum);
};

