// Report 2 — Vitals History (full time-series export)
// ═════════════════════════════════════════════════════════════════════════════

const buildVitalsHistory = (doc, { patient, readings, from, to, generatedBy, hospitalName }) => {
  let y       = pageHeader(doc, hospitalName);
  let pageNum = 1;

  y += 14;
  doc.font(FONT.bold).fontSize(14).fillColor(C.navy)
     .text("Vitals History Report", MARGIN, y);
  doc.font(FONT.regular).fontSize(8).fillColor(C.muted)
     .text(
       `Patient: ${patient.name} (${patient.patientID})   ·   ` +
       `Period: ${from ? fDate(from) : "All time"} – ${to ? fDate(to) : "Present"}   ·   ` +
       `Total readings: ${readings.length}   ·   Generated: ${fDateTime(new Date())}   ·   By: ${generatedBy}`,
       MARGIN, y + 18, { width: PW }
     );
  y += 42;
  hRule(doc, y);
  y += 14;

  if (readings.length === 0) {
    doc.font(FONT.regular).fontSize(10).fillColor(C.muted)
       .text("No vitals recorded for the selected period.", MARGIN, y);
    pageFooter(doc, pageNum);
    return;
  }

  const ROWS_PER_PAGE = 28;
  const chunks = [];
  for (let i = 0; i < readings.length; i += ROWS_PER_PAGE) {
    chunks.push(readings.slice(i, i + ROWS_PER_PAGE));
  }

  chunks.forEach((chunk, ci) => {
    if (ci > 0) {
      pageFooter(doc, pageNum++);
      doc.addPage();
      y = pageHeader(doc, hospitalName) + 20;
    }

    y = drawTable(doc, y, {
      headers:   ["Recorded At", "BP (mmHg)", "HR (bpm)", "Sugar (mg/dL)", "SpO₂ (%)", "Temp (°C)", "Weight (kg)", "Hospital"],
      rows:      chunk.map((r) => [
        fDateTime(r.recordedAt),
        `${r.bpSystolic}/${r.bpDiastolic}`,
        r.heartRate,
        r.sugar,
        r.spo2        ?? "—",
        r.temperature ?? "—",
        r.weight      ?? "—",
        r.hospital?.name ?? "—",
      ]),
      colWidths: [115, 65, 55, 75, 55, 55, 65, 60],
      rowHeight: 20,
    });
    y += 8;
  });

  pageFooter(doc, pageNum);
};

// ── Public API ────────────────────────────────────────────────────────────────

const createDoc = () =>
  new PDFDocument({ margin: MARGIN, size: "A4", autoFirstPage: true });

module.exports = { createDoc, buildPatientSummary, buildVitalsHistory };