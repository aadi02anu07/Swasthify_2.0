const PDFDocument = require("pdfkit");

// ── Design tokens ─────────────────────────────────────────────────────────────
const C = {
  navy:    "#1e3a5f",
  blue:    "#2563eb",
  bgLight: "#f1f5f9",
  bgAlt:   "#f8fafc",
  border:  "#cbd5e1",
  text:    "#1e293b",
  muted:   "#64748b",
  red:     "#dc2626",
  amber:   "#d97706",
  green:   "#16a34a",
  white:   "#ffffff",
};

const FONT   = { regular: "Helvetica", bold: "Helvetica-Bold" };
const MARGIN = 50;
const PW     = 595 - MARGIN * 2; // printable width on A4

// ── Low-level helpers ─────────────────────────────────────────────────────────

const hRule = (doc, y, color = C.border) => {
  doc.moveTo(MARGIN, y).lineTo(MARGIN + PW, y)
     .strokeColor(color).lineWidth(0.5).stroke();
  return y;
};

const sectionTitle = (doc, y, title) => {
  doc.rect(MARGIN, y, PW, 20).fill(C.navy);
  doc.font(FONT.bold).fontSize(9).fillColor(C.white)
     .text(title.toUpperCase(), MARGIN + 8, y + 6, { width: PW - 16 });
  return y + 20;
};

const labelValue = (doc, x, y, label, value, { labelWidth = 110 } = {}) => {
  doc.font(FONT.bold).fontSize(8).fillColor(C.muted)
     .text(label, x, y, { width: labelWidth });
  doc.font(FONT.regular).fontSize(8).fillColor(C.text)
     .text(String(value ?? "Not recorded"), x + labelWidth, y, { width: PW / 2 - labelWidth });
};

/**
 * Draws a table with shaded header and alternating row backgrounds.
 * Cells can be plain strings or { text, color } objects for coloured text.
 * Returns the Y position after the last row.
 */
const drawTable = (doc, y, { headers, rows, colWidths, rowHeight = 22, headerHeight = 26 }) => {
  const totalW = colWidths.reduce((a, b) => a + b, 0);

  // Header row
  doc.rect(MARGIN, y, totalW, headerHeight).fill(C.bgLight);
  let cx = MARGIN;
  headers.forEach((h, i) => {
    doc.font(FONT.bold).fontSize(8).fillColor(C.text)
       .text(h, cx + 6, y + Math.floor((headerHeight - 8) / 2), {
         width: colWidths[i] - 8, lineBreak: false,
       });
    cx += colWidths[i];
  });

  // Data rows
  rows.forEach((row, ri) => {
    const ry = y + headerHeight + ri * rowHeight;
    if (ri % 2 === 0) {
      doc.rect(MARGIN, ry, totalW, rowHeight).fill(C.bgAlt);
    }
    let cx = MARGIN;
    row.forEach((cell, ci) => {
      const text  = typeof cell === "object" ? (cell?.text ?? "—") : String(cell ?? "—");
      const color = typeof cell === "object" ? (cell?.color ?? C.text) : C.text;
      doc.font(FONT.regular).fontSize(7.5).fillColor(color)
         .text(text, cx + 6, ry + Math.floor((rowHeight - 7.5) / 2), {
           width: colWidths[ci] - 8, lineBreak: false,
         });
      cx += colWidths[ci];
    });
  });

  // Outer border
  doc.rect(MARGIN, y, totalW, headerHeight + rows.length * rowHeight)
     .strokeColor(C.border).lineWidth(0.5).stroke();

  // Column dividers
  cx = MARGIN;
  colWidths.slice(0, -1).forEach((w) => {
    cx += w;
    doc.moveTo(cx, y).lineTo(cx, y + headerHeight + rows.length * rowHeight)
       .strokeColor(C.border).lineWidth(0.3).stroke();
  });

  // Row dividers
  for (let ri = 0; ri <= rows.length; ri++) {
    const ry = y + headerHeight + ri * rowHeight;
    doc.moveTo(MARGIN, ry).lineTo(MARGIN + totalW, ry)
       .strokeColor(C.border).lineWidth(0.3).stroke();
  }

  return y + headerHeight + rows.length * rowHeight;
};

// ── Formatters ────────────────────────────────────────────────────────────────
const fDate = (d) =>
  d ? new Date(d).toLocaleDateString("en-IN", { day: "2-digit", month: "short", year: "numeric" }) : "—";

const fDateTime = (d) =>
  d ? new Date(d).toLocaleString("en-IN", {
    day: "2-digit", month: "short", year: "numeric",
    hour: "2-digit", minute: "2-digit",
  }) : "—";

const severityColor = (s) =>
  ({ severe: C.red, moderate: C.amber, mild: C.green }[s] ?? C.muted);

// ── Repeated page chrome ──────────────────────────────────────────────────────

const pageHeader = (doc, hospitalName) => {
  doc.rect(0, 0, 595, 48).fill(C.navy);
  doc.font(FONT.bold).fontSize(16).fillColor(C.white)
     .text("Swasthify", MARGIN, 14);
  doc.font(FONT.regular).fontSize(8).fillColor("#93c5fd")
     .text("Multi-Hospital Patient Record Network", MARGIN, 32);
  if (hospitalName) {
    doc.font(FONT.regular).fontSize(8).fillColor(C.white)
       .text(hospitalName, 0, 18, { align: "right", width: 595 - MARGIN });
  }
  return 62;
};

const pageFooter = (doc, pageNum) => {
  const y = 800;
  hRule(doc, y);
  doc.font(FONT.regular).fontSize(6.5).fillColor(C.muted)
     .text(
       "DISCLAIMER: This document is generated by Swasthify for use by qualified healthcare professionals only. " +
       "It does not constitute a medical diagnosis or prescription. Always exercise clinical judgment.",
       MARGIN, y + 6, { width: PW - 50 }
     );
  doc.font(FONT.regular).fontSize(7).fillColor(C.muted)
     .text(`Page ${pageNum}`, 0, y + 6, { align: "right", width: 595 - MARGIN });
};

